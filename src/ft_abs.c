#include "../fdf.h"

size_t	ft_abs(size_t nb)
{
	if ((long)nb < 0)
		return (-nb);
	return (nb);
}

void	ft_swap(float *a, float *b)
{
	float tmp;

	tmp = *a;
	*a = *b;
	*b = tmp;
}
